def addition(number_one, number_two):
    return int(number_one) + int(number_two)

def multiply(number_one, number_two):
    return int(number_one) * int(number_two)

def substract(number_one, number_two):
    return int(number_one) - int(number_two)

def division(number_one, number_two):
    return int(number_one) / int(number_two)
